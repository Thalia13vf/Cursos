package basico.operadores;

public class Matematicos {
	public static void main(String[] args) {
		System.out.println(0 + 1); //Adição
		
		System.out.println(3 - 1); //Subtração
		
		System.out.println(3 * 1); //Multiplicação
		
		System.out.println(8 / 2); //Divisão
		
		System.out.println(8 % 2); //modulo - resto da divisão
		
		var numero = 10;
		numero *= 2;
		System.out.println(numero);
	}
}
